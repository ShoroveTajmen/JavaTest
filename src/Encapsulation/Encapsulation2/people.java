package Encapsulation.Encapsulation2;

public class people {
    int height;
    int weight;
    String color;
    int age;

    public void display(){
        System.out.println("Height: " +this.height);
        System.out.println("Weight: " +this.weight);
        System.out.println("Color: " +this.color);
        System.out.println("Age: " +this.age);

    }
}
